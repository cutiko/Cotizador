package cl.pingon.cotizador.model;

public class MachinesDetails {

    private String configuration, electric_consumption, horizontal_reach, key, max_load, name, photo, vertical_reach, comments;

    public MachinesDetails() {
    }

    public String getConfiguration() {
        return configuration;
    }

    public void setConfiguration(String configuration) {
        this.configuration = configuration;
    }

    public String getElectric_consumption() {
        return electric_consumption;
    }

    public void setElectric_consumption(String electric_consumption) {
        this.electric_consumption = electric_consumption;
    }

    public String getHorizontal_reach() {
        return horizontal_reach;
    }

    public void setHorizontal_reach(String horizontal_reach) {
        this.horizontal_reach = horizontal_reach;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getMax_load() {
        return max_load;
    }

    public void setMax_load(String max_load) {
        this.max_load = max_load;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getVertical_reach() {
        return vertical_reach;
    }

    public void setVertical_reach(String vertical_reach) {
        this.vertical_reach = vertical_reach;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
}
